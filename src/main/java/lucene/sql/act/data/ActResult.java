package lucene.sql.act.data;

public class ActResult {

	private Integer status;
	private Object data;
	
	public ActResult() {
		super();
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}
}
